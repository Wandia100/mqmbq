document.addEventListener('DOMContentLoaded', function () {
    const chart = Highcharts.chart('pie-chart', {
        chart: {
            plotBackgroundColor: null,
            plotBorderWidth: null,
            plotShadow: false,
            type: 'pie'
        },
        title: {
            text: 'Transaction Statistics'
        },
        tooltip: {
            pointFormat: '{series.name}: <b>{point.percentage:.1f}%</b>'
        },
        accessibility: {
            point: {
                valueSuffix: '%'
            }
        },
        plotOptions: {
            pie: {
                allowPointSelect: true,
                cursor: 'pointer',
                dataLabels: {
                    enabled: true,
                    format: '<b>{point.name}</b>: {point.percentage:.1f} %'
                }
            }
        },
        series: [{
            name: 'Brands',
            colorByPoint: true,
            data: [{
                name: 'Amount Raised',
                y: parseInt(document.getElementById('percent_raised').innerHTML),
                sliced: true,
                selected: true
            }, {
                name: 'Amount Pending',
                y: parseInt(document.getElementById('percent_pending').innerHTML)
            }]
        }]
    });
});